    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        System.out.println("===> Edit Button clicked ...");
        
        Client client;
        ClientMapDAO clientMap = new ClientMapDAO();
        
        System.out.println("===> Getting text from fields ...");
        String name = nameField.getText();
        String phone = phoneField.getText();
        String cpf = cpfField.getText();
        Integer phoneInt;  
        long cpfLong;
        
        
        if(cpf.isBlank() || name.isBlank()){
            System.out.println("===> Name or CPF is blank ...");
            
            JOptionPane.showMessageDialog(null, "Provide at least \n name and cpf!");
        } else if(phone.isBlank()){
            System.out.println("===> Phone is blank ...");
            
            phoneInt = 000;
            cpfLong = Long.parseLong(cpf);
            client = new Client(name, cpfLong, phoneInt);
            clientMap.editClient(client);
            
        } else{
            System.out.println("===> None is Blank ...");
            
            String phoneAsString = phoneField.getText();
            Integer phoneAsInteger = Integer.parseInt(phoneAsString);
            
            phoneInt = Integer.parseInt(phone);
            cpfLong = Long.parseLong(cpf);
            client = new Client(name, cpfLong, phoneAsInteger);
            clientMap.editClient(client);
        }
        
    }//GEN-LAST:event_editButtonActionPerformed

