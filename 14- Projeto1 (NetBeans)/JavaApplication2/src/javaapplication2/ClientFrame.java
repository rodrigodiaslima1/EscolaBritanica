    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        
        String name = nameField.getText();
        String cpf = cpfField.getText();
        String phone = phoneField.getText();
        
        if((cpf.isBlank()) || (name.isBlank())){
            JOptionPane.showMessageDialog(null, "Please, provide name\n and cpf");
        }else if(phone.isBlank()){
            
            Long cpfLong = Long.parseLong(cpf);
            Client client = new Client(name, cpfLong);
            clientMap.registerClient(client);
//            JOptionPane.showMessageDialog(null, "Client registered with\n "
//                    + "name and cpf");


            System.out.println("===> Registered ...");
            
        } else{
            
            Integer phoneInt = Integer.parseInt(phone);
            Long cpfLong = Long.parseLong(cpf);
            Client client = new Client(name, cpfLong, phoneInt);
            clientMap.registerClient(client);
//            JOptionPane.showMessageDialog(null, "Client registered");
            System.out.println("===> Registered ...");
            
        }
        
    }//GEN-LAST:event_registerButtonActionPerformed

    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        String cpf = cpfField.getText();
        
        if(cpf.isBlank()){
            JOptionPane.showMessageDialog(null, "Please, provide cpf");
        }else{
            
            Long cpfLong = Long.parseLong(cpf);
        
        
        Client requestedClient = clientMap.searchClient(cpfLong);
        
        TableModel table = new TableModel(contentTable);
        table.writeOnTable(requestedClient);
            
        }
    }//GEN-LAST:event_searchButtonActionPerformed

